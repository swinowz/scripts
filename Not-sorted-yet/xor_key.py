def xor_decrypt(data, key):
    final = []
    for i in range(len(data)):
        final.append(chr(data[i] ^ ord(key[i % len(key)])))
    return ''.join(final)

bytes = [
    0x0b, 0x15, 0x3e, 0x14, 0x1e, 0x16, 0x21, 0x56, 0x26, 0x2c, 0x46, 0x0e, 0x4a, 0x14, 0x54, 0x0d,
    0x40, 0x13, 0x50, 0x20, 0x16, 0x47, 0x3b, 0x46, 0x09, 0x16, 0x01, 0x04
]

plaintext = xor_decrypt(bytes, "my_secret_key")
print(plaintext)